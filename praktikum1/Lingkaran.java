public class Lingkaran {
    
public static void main(String args[]) 
{
	float phi=3.14f;
	int r=10;
	float luas=phi*(r*r);
	float kel=2*phi*r;
	System.out.println("Phi                     : "+phi);
	System.out.println("Jari-jari               : "+r);
 	System.out.println("Luas Lingkaran          : "+luas);
	System.out.println("Keliling Lingkaran      : "+kel);
}
}